# ## Бинарный поиск (двусторонний закрытый интервал) ###
def binary_search(nums, target)
  # Инициализировать закрытый интервал [0, n-1], где i и j указывают на первый и последний элементы массива
  i, j = 0, nums.length - 1

  # Выполнять цикл и выйти, когда диапазон поиска станет пустым (при i > j он пуст)
  while i <= j
    # Теоретически числа в Ruby могут быть сколь угодно большими (ограничение зависит от объема памяти), поэтому проблему переполнения больших чисел можно не учитывать
    m = (i + j) / 2   # Вычислить средний индекс m
  
    if nums[m] < target
      i = m + 1 # Это означает, что target находится в интервале [m+1, j]
    elsif nums[m] > target
      j = m - 1 # Это означает, что target находится в интервале [i, m-1]
    else
      return m  # Целевой элемент найден, вернуть его индекс
    end
  end

  -1  # Целевой элемент не найден, вернуть -1
end

# ## Бинарный поиск (левый закрытый, правый открытый интервал) ###
def binary_search_lcro(nums, target)
  # Инициализировать интервал [0, n), где i и j указывают на первый элемент массива и позицию за последним элементом
  i, j = 0, nums.length

  # Выполнять цикл и выйти, когда диапазон поиска станет пустым (при i = j он пуст)
  while i < j
    # Вычислить средний индекс m
    m = (i + j) / 2

    if nums[m] < target
      i = m + 1 # Это означает, что target находится в интервале [m+1, j)
    elsif nums[m] > target
      j = m - 1 # Это означает, что target находится в интервале [i, m)
    else
      return m  # Целевой элемент найден, вернуть его индекс
    end
  end

  -1  # Целевой элемент не найден, вернуть -1
end

